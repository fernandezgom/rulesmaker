package com.rulesmaker.vo;

public class InitialConfiguration {
	
	private boolean lab;
	private boolean lines;
	private boolean rectangles;
	private boolean sets;
	private boolean liquids;
	private boolean colour;
	private boolean copy;
	private boolean equivalence;
	private boolean parent;
	private boolean highlight;
	private boolean add;
	private boolean substract;
	
	
	public boolean isLab() {
		return lab;
	}
	public void setLab(boolean lab) {
		this.lab = lab;
	}
	public boolean isLines() {
		return lines;
	}
	public void setLines(boolean lines) {
		this.lines = lines;
	}
	public boolean isRectangles() {
		return rectangles;
	}
	public void setRectangles(boolean rectangles) {
		this.rectangles = rectangles;
	}
	public boolean isSets() {
		return sets;
	}
	public void setSets(boolean sets) {
		this.sets = sets;
	}
	public boolean isLiquids() {
		return liquids;
	}
	public void setLiquids(boolean liquids) {
		this.liquids = liquids;
	}
	public boolean isColour() {
		return colour;
	}
	public void setColour(boolean colour) {
		this.colour = colour;
	}
	public boolean isCopy() {
		return copy;
	}
	public void setCopy(boolean copy) {
		this.copy = copy;
	}
	public boolean isEquivalence() {
		return equivalence;
	}
	public void setEquivalence(boolean equivalence) {
		this.equivalence = equivalence;
	}
	public boolean isParent() {
		return parent;
	}
	public void setParent(boolean parent) {
		this.parent = parent;
	}
	public boolean isHighlight() {
		return highlight;
	}
	public void setHighlight(boolean highlight) {
		this.highlight = highlight;
	}
	public boolean isAdd() {
		return add;
	}
	public void setAdd(boolean add) {
		this.add = add;
	}
	public boolean isSubstract() {
		return substract;
	}
	public void setSubstract(boolean substract) {
		this.substract = substract;
	}
	

}
